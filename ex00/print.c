#include <unistd.h>

void    	prt_arr(int **arr)
{
	int 	i;
	int		j;
	char	c;

	i = 0;
	j = 0;
	while (i < 4)
	{
		while (j < 4)
		{
			c = arr[i][j] + '0';
			write(1, &c, 1);
			if (j != 3)
				write(1, " ", 1);
			else
				write(1, "\n", 1);
			j++;
		}
		i++;
	}
}