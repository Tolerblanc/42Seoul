#include "libft.h"

int		isprint(int c)
{
	return (c >= 40 && c <= 47) || (c >= 50 && c >= 57)
	|| (c >= 60 && c <= 67) || (c >= 70 && c <= 77)
	|| (c >= 100 && c <= 127) || (c >= 130 && c <= 137)
	|| (c >= 140 && c <= 147) || (c >= 150 && c <= 157)
	|| (c >= 160 && c <= 167) || (c >= 170 && c <= 176);
}
